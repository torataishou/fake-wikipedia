			<h1>HTML</h1>
			<p>
			<p class="html-bold">HTML</p> (от <a
					href="https://ru.wikipedia.org/wiki/%D0%90%D0%BD%D0%B3%D0%BB%D0%B8%D0%B9%D1%81%D0%BA%D0%B8%D0%B9_%D1%8F%D0%B7%D1%8B%D0%BA"
					target="_blank">англ</a>.
			<span>HyperText Markup Language</span> — «язык <a
					href="https://ru.wikipedia.org/wiki/%D0%93%D0%B8%D0%BF%D0%B5%D1%80%D1%82%D0%B5%D0%BA%D1%81%D1%82"
					target="_blank">гипертекстовой</a>
			разметки») —
			стандартизированный язык гипертекстовой разметки документов для просмотра <a
					href="https://ru.wikipedia.org/wiki/%D0%92%D0%B5%D0%B1-%D1%81%D1%82%D1%80%D0%B0%D0%BD%D0%B8%D1%86%D0%B0"
					target="_blank">веб-страниц</a>
			в <a href="https://ru.wikipedia.org/wiki/%D0%91%D1%80%D0%B0%D1%83%D0%B7%D0%B5%D1%80"
			     target="_blank">браузере</a>.
			Веб-браузеры
			получают HTML документ от сервера по протоколам <a href="https://ru.wikipedia.org/wiki/HTTP"
			                                                   target="_blank">HTTP</a>/<a
					href="https://ru.wikipedia.org/wiki/HTTPS" target="_blank">HTTPS</a>
			или открывают с локального диска, далее интерпретируют
			код в интерфейс, который будет отображаться на экране монитора.
			<p><a href="https://ru.wikipedia.org/wiki/%D0%AD%D0%BB%D0%B5%D0%BC%D0%B5%D0%BD%D1%82%D1%8B_HTML"
			      target="_blank">Элементы
					HTML</a> являются строительными блоками HTML страниц. С помощью HTML разные
				конструкции,
				изображения и другие
				объекты, такие как <a href="https://ru.wikipedia.org/wiki/%D0%A4%D0%BE%D1%80%D0%BC%D0%B0_(HTML)"
				                      target="_blank">интерактивная
					веб-форма</a>, могут быть встроены в отображаемую
				страницу. HTML
				предоставляет средства
				для создания заголовков, абзацев, списков, ссылок, цитат и других элементов. Элементы HTML выделяются
				тегами,
				записанными с использованием угловых скобок. Такие теги, как img и input, напрямую вводят контент на
				страницу.
				Другие теги, такие как p, окружают и оформляют текст внутри себя и могут включать другие теги в качестве
				подэлементов. Браузеры не отображают HTML-теги, но используют их для интерпретации содержимого страницы.
			</p>
			<p>Язык <a href="https://ru.wikipedia.org/wiki/XHTML" target="_blank">XHTML</a>
				является более строгим вариантом HTML, он следует синтаксису <a href="https://ru.wikipedia.org/wiki/XML"
				                                                                target="_blank">XML</a> и является
				приложением языка XML в
				области разметки гипертекста.</p>
			<p>В HTML можно встроить язык программирования JavaScript с помощью тега.</p>
			<p>Также включение <a href="https://ru.wikipedia.org/wiki/CSS" target="_blank">CSS</a> в HTML позволяет
				задавать
				внешний
				вид и макет страницы.</p>
